/* ================================================================
 * RepUp — GitHub Code Review Sidebar (content script)
 * ----------------------------------------------------------------
 * Injects a Shadow-DOM sidebar into github.com pages that:
 *   • Detects when the user is on a blob (single file) or a PR /files page
 *   • Pulls the actual code via GitHub REST (NOT DOM scraping — survives
 *     any GitHub front-end refactor)
 *   • Hands the code to /api/review on the RepUp backend, which streams
 *     NDJSON events (bugs / security / performance / smells / grade)
 *   • Caches every result keyed by the file's git SHA in
 *     chrome.storage.local so reopening the same file is instant
 *   • Tracks SPA navigation (history.pushState + popstate + a title
 *     MutationObserver fallback) so it switches files without a reload
 *   • Rate-limits: one auto-review per (URL, sha) pair, debounced 1.5s
 *
 * We isolate every byte of DOM/CSS in a Shadow Root so we can't break
 * GitHub's layout, and they can't break ours.
 * ================================================================ */
