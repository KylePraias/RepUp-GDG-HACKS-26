  function debounce(fn, ms) {
    let t = null;
    return (...args) => {
      if (t) clearTimeout(t);
      t = setTimeout(() => {
        t = null;
        fn(...args);
      }, ms);
    };
  }

  function detectLanguage(filename) {
    const ext = (filename || "").split(".").pop().toLowerCase();
    const map = {
      js: "javascript", jsx: "javascript", mjs: "javascript", cjs: "javascript",
      ts: "typescript", tsx: "typescript",
      py: "python", pyi: "python",
      go: "go", rs: "rust", java: "java", kt: "kotlin", swift: "swift",
      rb: "ruby", php: "php", cs: "csharp", c: "c", h: "c",
      cpp: "cpp", cc: "cpp", cxx: "cpp", hpp: "cpp",
      sh: "bash", bash: "bash", zsh: "bash",
      sql: "sql", html: "html", css: "css", scss: "scss",
      json: "json", yaml: "yaml", yml: "yaml", toml: "toml", md: "markdown",
    };
    return map[ext] || "auto";
  }

  // -------- chrome.storage.local cache (SHA-keyed) -----------------------
  // We persist by file SHA so a closed-and-reopened sidebar on the same
  // file commit hits instantly with NO backend round-trip.
  const cache = {
    async get(sha) {
      if (!sha) return null;
      const k = CACHE_KEY_PREFIX + sha;
      try {
        const obj = await chrome.storage.local.get(k);
        const entry = obj[k];
        if (!entry) return null;
        if (Date.now() - entry.ts > CACHE_TTL_MS) {
          chrome.storage.local.remove(k).catch(() => {});
          return null;
        }
        return entry;
      } catch (e) {
        log("cache.get failed", e);
        return null;
      }
    },
    async put(sha, payload) {
      if (!sha) return;
      const k = CACHE_KEY_PREFIX + sha;
      try {
        await chrome.storage.local.set({ [k]: { ts: Date.now(), ...payload } });
        // LRU-ish trim: if we exceed max entries, drop the oldest.
        const all = await chrome.storage.local.get(null);
        const keys = Object.keys(all).filter((x) => x.startsWith(CACHE_KEY_PREFIX));
        if (keys.length > CACHE_MAX_ENTRIES) {
          keys
            .map((kk) => [kk, all[kk]?.ts || 0])
            .sort((a, b) => a[1] - b[1])
            .slice(0, keys.length - CACHE_MAX_ENTRIES)
            .forEach(([kk]) => chrome.storage.local.remove(kk).catch(() => {}));
        }
      } catch (e) {
        log("cache.put failed", e);
      }
    },
    async clear() {
      try {
        const all = await chrome.storage.local.get(null);
        const keys = Object.keys(all).filter((x) => x.startsWith(CACHE_KEY_PREFIX));
        if (keys.length) await chrome.storage.local.remove(keys);
      } catch (e) {
        log("cache.clear failed", e);
      }
    },
  };

  // -------- GitHub URL → fetch plan --------------------------------------
  // Returns { kind, owner, repo, ref?, path?, prNumber? } or null.
  function parseGhUrl(href) {
    let u;
    try {
      u = new URL(href);
    } catch {
      return null;
    }
    if (u.hostname !== "github.com") return null;
    const parts = u.pathname.split("/").filter(Boolean);
    if (parts.length < 2) return null;
    const [owner, repo, ...rest] = parts;
    // Reserved usernames (settings, marketplace, …) — skip.
    if (["marketplace", "settings", "notifications", "explore", "trending", "topics"].includes(owner)) {
      return null;
    }
    if (rest[0] === "blob" && rest.length >= 3) {
      const ref = rest[1];
      const path = rest.slice(2).join("/");
      return { kind: "blob", owner, repo, ref, path };
    }
    if (rest[0] === "pull" && rest[2] === "files") {
      return { kind: "pr", owner, repo, prNumber: rest[1] };
    }
    return null;
  }

  // -------- GitHub REST helpers ------------------------------------------
  async function ghFetch(apiPath) {
    const r = await fetch(`https://api.github.com${apiPath}`, {
      headers: { Accept: "application/vnd.github+json" },
      credentials: "omit",
    });
    if (!r.ok) {
      const txt = await r.text().catch(() => "");
      const err = new Error(`GitHub ${r.status}: ${txt.slice(0, 140)}`);
      err.status = r.status;
      throw err;
    }
    return r.json();
  }

  async function loadBlobFile(plan) {
    // /repos/{owner}/{repo}/contents/{path}?ref={ref}
    const data = await ghFetch(
      `/repos/${plan.owner}/${plan.repo}/contents/${plan.path}?ref=${encodeURIComponent(plan.ref)}`,
    );
    if (Array.isArray(data) || !data.content) {
      throw new Error("Not a regular file (directory or symlink?)");
    }
    // Base64 with embedded newlines per RFC 2045.
    let code;
    try {
      const bin = atob(data.content.replace(/\s/g, ""));
      // The blob may contain non-ASCII bytes; decode as UTF-8.
      const bytes = new Uint8Array(bin.length);
      for (let i = 0; i < bin.length; i++) bytes[i] = bin.charCodeAt(i);
      code = new TextDecoder("utf-8").decode(bytes);
    } catch (e) {
      throw new Error(`Decode failed: ${e.message}`);
    }
    return {
      kind: "blob",
      owner: plan.owner,
      repo: plan.repo,
      filename: data.name,
      path: data.path,
      sha: data.sha,
      language: detectLanguage(data.name),
      code,
      sizeBytes: data.size || code.length,
    };
  }

  async function loadPrFiles(plan) {
    // /repos/{owner}/{repo}/pulls/{n}/files
    const arr = await ghFetch(
      `/repos/${plan.owner}/${plan.repo}/pulls/${plan.prNumber}/files?per_page=100`,
    );
    return {
      kind: "pr",
      owner: plan.owner,
      repo: plan.repo,
      prNumber: plan.prNumber,
      files: arr.map((f) => ({
        filename: f.filename,
        sha: f.sha, // blob SHA AT THIS COMMIT — perfect cache key
        status: f.status,
        additions: f.additions,
        deletions: f.deletions,
        patch: f.patch || "",
        rawUrl: f.raw_url,
        language: detectLanguage(f.filename),
      })),
    };
  }

  // -------- /api/review NDJSON consumer ----------------------------------
  // Streams events from the backend and routes them to the sidebar's
  // section renderer. Resolves with the final aggregated payload (which we
  // then persist to the SHA-keyed cache).
  //
  // IMPORTANT: We do NOT call fetch() directly here. github.com ships a
  // strict `connect-src` Content-Security-Policy that does NOT include
  // our backend, and Chrome MV3 content-script fetches DO respect the
  // host page's CSP. So we proxy the request through the background
  // service worker (which has its own extension CSP) via a long-lived
  // chrome.runtime.Port. The SW does the fetch and pipes raw NDJSON
  // chunks back; we parse them line-by-line right here.
  function streamReview({ code, language, filename, context, sha }, onEvent, signal) {
    return new Promise((resolve, reject) => {
      let port;
      try {
        port = chrome.runtime.connect({ name: "repup-review" });
      } catch (e) {
        reject(new Error(`Could not reach the RepUp background worker: ${e.message}. ` +
          `Try reloading the extension at chrome://extensions.`));
        return;
      }

      const aggregate = {
        meta: null,
        bugs: [],
        security: [],
        performance: [],
        smells: [],
        grade: null,
      };
      let buf = "";
      let settled = false;

      const finish = (err, value) => {
        if (settled) return;
        settled = true;
        try { port.disconnect(); } catch (_) { /* already gone */ }
        if (signal) signal.removeEventListener("abort", onAbort);
        err ? reject(err) : resolve(value);
      };

      const onAbort = () => {
        finish(Object.assign(new Error("aborted"), { name: "AbortError" }));
      };
      if (signal) {
        if (signal.aborted) { onAbort(); return; }
        signal.addEventListener("abort", onAbort, { once: true });
      }

      port.onMessage.addListener((msg) => {
        if (!msg) return;
        if (msg.type === "error") {
          finish(new Error(msg.message || "Backend review error"));
          return;
        }
        if (msg.type === "end") {
          finish(null, aggregate);
          return;
        }
        if (msg.type !== "chunk" || typeof msg.text !== "string") return;
        buf += msg.text;
        let nl;
        while ((nl = buf.indexOf("\n")) >= 0) {
          const line = buf.slice(0, nl).trim();
          buf = buf.slice(nl + 1);
          if (!line) continue;
          try {
            const ev = JSON.parse(line);
            if (ev.event === "error") {
              finish(new Error(ev.data?.message || "Backend review error"));
              return;
            }
            if (ev.event in aggregate) aggregate[ev.event] = ev.data;
            try { onEvent && onEvent(ev.event, ev.data, aggregate); } catch (e) { log("onEvent threw", e); }
          } catch (e) {
            log("bad ndjson line", line, e);
          }
        }
      });

      port.onDisconnect.addListener(() => {
        // If the SW disconnects without sending {type:'end'}, treat as error
        // unless we already settled. lastError surfaces "the message port
        // closed before a response was received" type situations.
        const err = chrome.runtime.lastError;
        if (!settled) finish(new Error(err?.message || "Background worker disconnected before completing review."));
      });

      port.postMessage({
        type: "start",
        url: BACKEND_URL + REVIEW_PATH,
        body: { code, language, filename, context, sha },
      });
    });
  }

