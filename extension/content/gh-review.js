  const ICONS = { bugs: "🐛", security: "🔒", performance: "⚡", smells: "🧹" };
  const LABELS = {
    bugs: "Bugs",
    security: "Security",
    performance: "Performance",
    smells: "Code smells",
  };

  function escapeHtml(s) {
    return String(s || "").replace(
      /[&<>"']/g,
      (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" })[c],
    );
  }

  function renderFinding(f) {
    const sev = (f.severity || "low").toLowerCase();
    const line = f.line != null ? `<span class="line">L${escapeHtml(f.line)}</span>` : "";
    return `
      <div class="finding">
        <div class="title">
          <span class="sev ${sev}">${sev}</span>
          <span style="flex:1">${escapeHtml(f.title || "Untitled")}</span>
          ${line}
        </div>
        ${f.description ? `<div class="desc">${escapeHtml(f.description)}</div>` : ""}
        ${f.suggestion ? `<div class="sug"><span class="sug-label">Fix</span>${escapeHtml(f.suggestion)}</div>` : ""}
      </div>
    `;
  }

  function renderSection(key, findings) {
    const arr = Array.isArray(findings) ? findings : [];
    return `
      <div class="section" data-section="${key}">
        <div class="section-head">
          <span class="icon">${ICONS[key]}</span>
          <span>${LABELS[key]}</span>
          <span class="badge">${arr.length}</span>
        </div>
        ${
          arr.length === 0
            ? `<div class="empty">All clear here ✨</div>`
            : arr.map(renderFinding).join("")
        }
      </div>
    `;
  }

  function renderGrade(g) {
    if (!g) return "";
    const letter = (g.letter || "C").toUpperCase();
    const cls = letter[0]; // A/B/C/D/F
    return `
      <div class="grade-card">
        <div class="grade-letter ${cls}">${escapeHtml(letter)}</div>
        <div class="grade-meta">
          <div class="grade-score">Score · ${escapeHtml(g.score ?? 0)}/100</div>
          <div class="grade-summary">${escapeHtml(g.summary || "")}</div>
        </div>
      </div>
    `;
  }

  function renderSkeletons() {
    return ["bugs", "security", "performance", "smells"]
      .map(
        (k) => `
        <div class="section">
          <div class="section-head">
            <span class="icon">${ICONS[k]}</span>
            <span>${LABELS[k]}</span>
            <span class="badge">…</span>
          </div>
          <div class="skel"></div>
          <div class="skel"></div>
        </div>`,
      )
      .join("");
  }

